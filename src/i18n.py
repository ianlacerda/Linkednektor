current_lang = "en"


def set_language(lang):
    """Sets the active language configuration."""
    global current_lang
    if lang in TRANSLATIONS:
        current_lang = lang


def get_language():
    """Gets the active language configuration."""
    return current_lang


def t(key, **kwargs):
    """Returns the translation for the key in the current language."""
    text = TRANSLATIONS.get(current_lang, TRANSLATIONS["en"]).get(key, key)
    if kwargs:
        text = text.format(**kwargs)
    return text
